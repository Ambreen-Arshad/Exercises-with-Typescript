// Name Cases: Store a person’s name in a variable, and then print that person’s name in lowercase, \n
// uppercase, and titlecase.

let personName : string = "ambreen";
console.log(personName.toLowerCase()); // shows the personName in all small letters.

console.log(personName.toUpperCase()); // shows the personName in all Big letters.

console.log(personName.charAt(0).toUpperCase() + personName.slice(1).toLowerCase()); // shows the personName with the first letter Big.